#include <iostream>
using namespace std;

int main()
{
    int arrayList1[4] = {1, 2, 3, 4};

    cout << arrayList1[0] << endl; // OUTPUTS THE FIRST ELEMENT OF THE ARRAY, WHICH IS 1
    cout << arrayList1[1] << endl; // OUTPUTS THE SECOND ELEMENT OF THE ARRAY, WHICH IS 2
    cout << arrayList1[2] << endl; // OUTPUTS THE THIRD ELEMENT OF THE ARRAY, WHICH IS 3
    cout << arrayList1[3] << endl; // OUTPUTS THE FOURTH ELEMENT OF THE ARRAY, WHICH IS 4

    return 0;
}